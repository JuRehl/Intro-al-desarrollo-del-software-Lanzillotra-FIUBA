const color = "red"

switch(color) {
    case "red": {
        console.log("El color es rojo")
        break
    }
    case "blue": {
        console.log("El color es azul")
        break
    }
    case "yellow": {
        console.log("El color es amarillo")
        break
    }
    default: {
        console.log("El color no existe")
    }    
}
